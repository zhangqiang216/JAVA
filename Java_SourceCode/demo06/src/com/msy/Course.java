package com.msy;

public class Course {
	private String cno;
	private String cname;
	private int ccridit;
	public String getCno() {
		return cno;
	}
	public void setCno(String cno) {
		this.cno = cno;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public int getCcridit() {
		return ccridit;
	}
	public void setCcridit(int ccridit) {
		this.ccridit = ccridit;
	}
	

}
